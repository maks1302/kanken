$(function() {
	$('#carousel').carouFredSel({
		items: 1,
		auto: false,
		scroll: {
			fx: 'none'
		},
		pagination: {
			container: '#pager',
			event: 'mouseenter',
			anchorBuilder: function(nr) {
				var src = $(this).attr('src').replace('/item_img/', '/item_img/');
				return '<img src="'+ src +'" border="0" />';
			}
		}				
	});
});

